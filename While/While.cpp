#include <bits/stdc++.h>

using namespace std;

int main() {
    int n;

    cin >> n;

    int i = 1;

    while (i <= n) {
        cout << "Kaizer" << endl;

        i++;
    }

    int j = n;

    while (j > 0) {
        cout << "Williams" << endl;

        j--;
    }

    return 0;
}